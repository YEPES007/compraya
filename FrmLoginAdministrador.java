    public void validarAdmin(){
    int resultado=0;
    Connection con =getConection();
        try {
            String usuario=txtUsuario.getText();
            String password=String.valueOf(txtPassword.getPassword());
            String sql="SELECT * FROM administrador WHERE usuario='"+usuario+"' and password='"+password+"'";
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(sql);
            if (rs.next()) {
                resultado=1;
                if (resultado==1) {
                    FrmVentas ventas=new FrmVentas();
                    ventas.setVisible(true);
                    this.dispose();
                    //FrmVentas.txtVendedor.setText(txtUsuario.getText());                    
                }                
            } else {
                JOptionPane.showMessageDialog(null,"Error, vuelve a intentarlo");
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error en el acceso, vuelve a intentarlo");
        }
    }
            
