    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection con;
        if (txtCodigo.getText().equals("")
        || txtProducto.getText().equals("")
        ||txtDescripcion.getText().equals("")
        ||txtCantidad.getText().equals("")
        ||txtCosto.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"No se puede Registrar el producto");
        } else {
            try{ 
                con=getConection();
                Statement st=con.createStatement();
                String primarykey="SELECT * FROM productos WHERE Codigo="+txtCodigo.getText()+" ";
                ResultSet rs=st.executeQuery(primarykey);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null,"Error el codigo de producto ya existe");
                    
                } else {
                    ps=con.prepareStatement("call Agregarproducto(?,?,?,?,?)");
                    ps.setString(1,txtCodigo.getText());
                    ps.setString(2,txtProducto.getText());
                    ps.setString(3,txtDescripcion.getText());
                    ps.setInt(4,Integer.parseInt(txtCantidad.getText()));
                    ps.setString(5,txtCosto.getText());
                    int res=ps.executeUpdate();
                    if (res>0){
                        JOptionPane.showMessageDialog(null,"producto registrado");   
                        limpiarCajas();
    
                    }else{
                        JOptionPane.showMessageDialog(null, "Error al registrar el producto");
                        limpiarCajas();
                    }
                    mostrarDatos();
                    con.close();
                }
                        
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error al conectar");
                
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Connection con;
        if (txtCodigo.getText().equals("")
                || txtProducto.getText().equals("")
                ||txtDescripcion.getText().equals("")
                ||txtCantidad.getText().equals("")
                ||txtCosto.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"no se ah seleccionado ningun producto");
            
        } else {
            int opc=JOptionPane.showConfirmDialog(this, "Estas seguro de modificar los cambios del producto?","Pregunta",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if (opc==JOptionPane.YES_OPTION) {
                try {
                    con=getConection();
                    ps=con.prepareStatement("call ModificarProducto(?,?,?,?,?)");
                    ps.setString(1,txtCodigo.getText());
                    ps.setString(2,txtProducto.getText());
                    ps.setString(3,txtDescripcion.getText());
                    ps.setInt(4,Integer.parseInt(txtCantidad.getText()));
                    ps.setInt(5,Integer.parseInt(txtCosto.getText()));
                    int res=ps.executeUpdate();
                    if (res>0) {
                        JOptionPane.showMessageDialog(null,"se ah modificado el producto");
                        limpiarCajas();
                    } else {
                        JOptionPane.showMessageDialog(null,"Error al modificar el producto");
                        limpiarCajas();
                    }
                    mostrarDatos();
                    con.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error al conectar");
                }
                
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    
