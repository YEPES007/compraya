    private void txtBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyTyped
        String[] titulos={"Codigo","Producto","Descripcion","Cantidad","Costo"};
        Connection con;
        String[] registros=new String[50];
        String sql="SELECT* FROM productos WHERE Codigo LIKE '%"+txtBuscar.getText()+"%'"
                +"OR Producto LIKE '%"+txtBuscar.getText()+"%'"
                +"OR Descripcion LIKE '%"+txtBuscar.getText()+"%'";
        DefaultTableModel modelo=new DefaultTableModel(null,titulos);
        try {
            con=getConection();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while (rs.next()) {
                registros[0]=rs.getString("Codigo");
                registros[1]=rs.getString("Producto");
                registros[2]=rs.getString("Descripcion");
                registros[3]=rs.getString("Cantidad");
                registros[4]=rs.getString("Costo");
                modelo.addRow(registros);
            }
            tbProductos.setModel(modelo);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtBuscarKeyTyped

